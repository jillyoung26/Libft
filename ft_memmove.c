#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t len)
{
	size_t	i;
	char	*srcc;
	char	*dstc;

	srcc = (char *)src;
	dstc = (char *)dest;
	i = -1;
	if (!dest && !src)
		return (dest);
	if (srcc < dstc)
		while ((int)(--len) >= 0)
			*(dstc + len) = *(srcc + len);
	else
		while (++i < len)
			*(dstc + i) = *(srcc + i);
	return (dest);
}
